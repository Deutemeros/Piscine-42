#include <stdio.h>

int	ft_is_prime(int nb)
{
	int val;

	val = nb - 1;
	if (nb < 1)
		return (0);
	while (nb % val != 0 && val > 1)
		val--;
	if (val == 1)
		return (1);
	else
		return (0);
}
