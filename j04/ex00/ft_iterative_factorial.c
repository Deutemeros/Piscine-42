int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (i > 0 && i < 13)
	{
		while (i > 1 && i < 13)
		{
			i--;
			nb = nb * i;
		}
	}
	else if (i == 0)
		nb = 1;
	else
		return (0);
	return (nb);
}
