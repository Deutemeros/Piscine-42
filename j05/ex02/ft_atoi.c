int	ft_atoi(char *str)
{
	int	i;
	int	var;
	int	n;

	n = 1;
	var = 0;
	i = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n') ||
			(str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		var *= 10;
		var += (str[i] - '0');
		i++;
	}
	var *= n;
	return (var);
}
