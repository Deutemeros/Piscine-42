char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int	i;
	int	l;

	i = 0;
	l = 0;
	while (dest[l] != '\0')
		l++;
	while (i < nb)
	{
		dest[l] = src[i];
		i++;
		l++;
	}
	return (dest);
}
