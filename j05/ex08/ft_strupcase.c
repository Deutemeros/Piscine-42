#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int	l;

	l = 0;
	while (str[l] != '\0')
		l++;
	while (l >= 0)
	{
		if (str[l] >= 'a' && str[l] <= 'z')
			str[l] += 'A' - 'a';
		l--;
	}
	return (str);
}
