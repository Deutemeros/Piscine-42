char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
		i++;
	dest[i + 1] = '\0';
	while (i > 0)
	{
		dest[i] = src[i];
		i--;
	}
	return (dest);
}
